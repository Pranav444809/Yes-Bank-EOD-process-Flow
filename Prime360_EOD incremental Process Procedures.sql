create or replace PROCEDURE PSX_VALIDATE_ENPA_DATA_PROC (
    v_psx_batch_id IN VARCHAR2, 
    v_status OUT VARCHAR2,
    v_description OUT VARCHAR2,
    v_sqlerror OUT VARCHAR2
) AS
    v_enpa_count NUMBER;
    v_error_location VARCHAR2(100);
BEGIN
    -- Enable parallel DML for the session
    EXECUTE IMMEDIATE 'ALTER SESSION ENABLE PARALLEL DML';

    v_error_location := 'Start';
    INSERT /*+ PARALLEL(6) APPEND */ INTO PSX_PRIME_EOD_STATUS (
        psx_batch_id, description, STATUS, MODULE, INSERT_TIME
    ) VALUES (
        v_psx_batch_id, 'ENPA Data validation and temp insertion procedure started', 'C', 'ENPA_VALIDATE_TEMP_PROC', CURRENT_TIMESTAMP
    );
    COMMIT; -- Commit status insert (small table, no conflict)


  -- Original logic: Count total raw rows in YBL_ENPA_DATA_INCREMENTAL
    v_error_location := 'Count raw data';
    SELECT /*+ PARALLEL(6) */ COUNT(*) INTO v_enpa_count 
    FROM YESBANK_DEV.YBL_ENPA_DATA_INCREMENTAL;

    v_error_location := 'Check ENPA data existence';
    IF v_enpa_count > 0 THEN
         INSERT /*+ PARALLEL(6) APPEND */ INTO PSX_PRIME_EOD_STATUS (
        psx_batch_id, description, STATUS, MODULE, INSERT_TIME
    ) VALUES (
        v_psx_batch_id, 'ENPA data found for processing', 'C', 'ENPA_VALIDATE_TEMP_PROC', CURRENT_TIMESTAMP
    );
        v_error_location := 'Update psx_batch_id';
        UPDATE /*+ PARALLEL(6) */ YESBANK_DEV.YBL_ENPA_DATA_INCREMENTAL 
        SET psx_batch_id = v_psx_batch_id;
        COMMIT; -- Commit to allow subsequent read from YBL_ENPA_DATA_INCREMENTAL

        v_error_location := 'Insert invalid source systems';
        INSERT /*+ PARALLEL(6) APPEND */ INTO YESBANK_DEV.YBL_ENPA_DATA_INCREMENTAL_ERROR (
            PSX_BATCH_ID, ERROR_CAUSE, BANK_UCIC, CUST_UNQ_ID, DATEOFDATA, ENPA_CLASSIFICATION, 
            FCR_CUSTOMER_ID, INSERT_TS, SOURCE_ASSET_CLASSIFICATION, SOURCE_SYSTEM, 
            SOURCE_SYSTEM_ACCOUNT_NO, SOURCE_SYSTEM_CUSTID, SOURCE_SYSTEM_ID
        )
        SELECT /*+ PARALLEL(6) INDEX(YBL idx_ybl_enpa_source_system) */ 
            v_psx_batch_id, 'Invalid Source System Name', BANK_UCIC, CUST_UNQ_ID, DATEOFDATA, 
            ENPA_CLASSIFICATION, FCR_CUSTOMER_ID, INSERT_TS, SOURCE_ASSET_CLASSIFICATION, 
            SOURCE_SYSTEM, SOURCE_SYSTEM_ACCOUNT_NO, SOURCE_SYSTEM_CUSTID, SOURCE_SYSTEM_ID
        FROM YESBANK_DEV.YBL_ENPA_DATA_INCREMENTAL YBL 
        WHERE NOT EXISTS (
            SELECT /*+ INDEX(SSM idx_ssm_source_system_name) */ 1 
            FROM SOURCE_SYSTEM_MASTER SSM 
            WHERE SSM.source_system_name = YBL.source_system
        );
        COMMIT; -- Commit to allow subsequent delete on YBL_ENPA_DATA_INCREMENTAL

        v_error_location := 'Delete invalid source systems';
        DELETE /*+ PARALLEL(6) */ FROM YESBANK_DEV.YBL_ENPA_DATA_INCREMENTAL YBL
        WHERE NOT EXISTS (
            SELECT /*+ INDEX(SSM idx_ssm_source_system_name) */ 1 
            FROM SOURCE_SYSTEM_MASTER SSM 
            WHERE SSM.source_system_name = YBL.source_system
        );
        COMMIT; -- Commit to allow subsequent read into PSX_ENPA_DATA_TEMP

        v_error_location := 'Insert into temp table';
        INSERT /*+ PARALLEL(6) APPEND */ INTO PSX_ENPA_DATA_TEMP (
            SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, SOURCE_SYSTEM_CUSTID, BANK_UCIC, 
            SOURCE_SYSTEM, ENPA_CLASSIFICATION, SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, 
            INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID, PSX_BATCH_ID
        )
        SELECT /*+ PARALLEL(6) */ 
            SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, SOURCE_SYSTEM_CUSTID, BANK_UCIC, 
            SOURCE_SYSTEM, ENPA_CLASSIFICATION, SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, 
            INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID, v_psx_batch_id
        FROM YESBANK_DEV.YBL_ENPA_DATA_INCREMENTAL;
        COMMIT; -- Commit to allow subsequent merge on PSX_ENPA_DATA_TEMP

        v_error_location := 'Update SOURCE_SYSTEM_ID and CUST_UNQ_ID';
        MERGE /*+ PARALLEL(6) */ INTO PSX_ENPA_DATA_TEMP PEI
        USING (
            SELECT /*+ PARALLEL(6) INDEX(YBL idx_enpa_temp_source_system) */ 
                   SSM.SOURCE_SYSTEM_ID, YBL.SOURCE_SYSTEM_CUSTID, YBL.ROWID AS rid
            FROM PSX_ENPA_DATA_TEMP YBL
            JOIN SOURCE_SYSTEM_MASTER SSM 
            ON SSM.SOURCE_SYSTEM_NAME = YBL.SOURCE_SYSTEM
        ) SRC
        ON (PEI.ROWID = SRC.rid)
        WHEN MATCHED THEN
            UPDATE SET 
                PEI.SOURCE_SYSTEM_ID = SRC.SOURCE_SYSTEM_ID,
                PEI.CUST_UNQ_ID = SRC.SOURCE_SYSTEM_ID || '_' || SRC.SOURCE_SYSTEM_CUSTID;
        COMMIT; -- Commit to allow subsequent duplicate handling

        v_error_location := 'Handle duplicates with same insert_ts';
        INSERT /*+ PARALLEL(6) APPEND */ INTO PSX_ENPA_DATA_INCREMENTAL_ERROR (
            PSX_BATCH_ID, ERROR_DESCRIPTION, SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, 
            SOURCE_SYSTEM_CUSTID, BANK_UCIC, SOURCE_SYSTEM, ENPA_CLASSIFICATION, 
            SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID
        )
        WITH duplicates AS (
            SELECT /*+ PARALLEL(6) INDEX(PSX_ENPA_DATA_TEMP idx_enpa_temp_cust_unq_id_ins_ts) */
                   SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, SOURCE_SYSTEM_CUSTID, BANK_UCIC, 
                   SOURCE_SYSTEM, ENPA_CLASSIFICATION, SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, 
                   INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID,
                   ROW_NUMBER() OVER (PARTITION BY CUST_UNQ_ID,SOURCE_SYSTEM_ACCOUNT_NO,INSERT_TS ORDER BY INSERT_TS) AS row_num
            FROM PSX_ENPA_DATA_TEMP
        )
        SELECT /*+ PARALLEL(6) */ 
            v_psx_batch_id, 'CUST_UNQ_ID duplicates with same insert_ts', 
            SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, SOURCE_SYSTEM_CUSTID, BANK_UCIC, 
            SOURCE_SYSTEM, ENPA_CLASSIFICATION, SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, 
            INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID
        FROM duplicates
        WHERE row_num > 1;
        COMMIT; -- Commit to allow delete on PSX_ENPA_DATA_TEMP

        DELETE /*+ PARALLEL(6) */ FROM PSX_ENPA_DATA_TEMP
        WHERE ROWID IN (
            SELECT rid
            FROM (
                SELECT /*+ PARALLEL(6) INDEX(PSX_ENPA_DATA_TEMP idx_enpa_temp_cust_unq_id_ins_ts) */
                       ROWID AS rid,
                       ROW_NUMBER() OVER (PARTITION BY CUST_UNQ_ID,SOURCE_SYSTEM_ACCOUNT_NO,INSERT_TS ORDER BY INSERT_TS) AS row_num
                FROM PSX_ENPA_DATA_TEMP
            ) WHERE row_num > 1
        );
        COMMIT; -- Commit to allow next duplicate handling

        v_error_location := 'Handle duplicates with different insert_ts';
        INSERT /*+ PARALLEL(6) APPEND */ INTO PSX_ENPA_DATA_INCREMENTAL_ERROR (
            PSX_BATCH_ID, ERROR_DESCRIPTION, SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, 
            SOURCE_SYSTEM_CUSTID, BANK_UCIC, SOURCE_SYSTEM, ENPA_CLASSIFICATION, 
            SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID
        )
        WITH duplicates AS (
            SELECT /*+ PARALLEL(6) INDEX(PSX_ENPA_DATA_TEMP idx_enpa_temp_cust_unq_id_ins_ts) */
                   SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, SOURCE_SYSTEM_CUSTID, BANK_UCIC, 
                   SOURCE_SYSTEM, ENPA_CLASSIFICATION, SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, 
                   INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID,
                   ROW_NUMBER() OVER (PARTITION BY CUST_UNQ_ID,SOURCE_SYSTEM_ACCOUNT_NO ORDER BY INSERT_TS DESC) AS row_num
            FROM PSX_ENPA_DATA_TEMP
        )
        SELECT /*+ PARALLEL(6) */ 
            v_psx_batch_id, 'CUST_UNQ_ID duplicates with diff insert_ts', 
            SOURCE_SYSTEM_ACCOUNT_NO, FCR_CUSTOMER_ID, SOURCE_SYSTEM_CUSTID, BANK_UCIC, 
            SOURCE_SYSTEM, ENPA_CLASSIFICATION, SOURCE_ASSET_CLASSIFICATION, DATEOFDATA, 
            INSERT_TS, SOURCE_SYSTEM_ID, CUST_UNQ_ID
        FROM duplicates
        WHERE row_num > 1;
        COMMIT; -- Commit to allow final delete

        DELETE /*+ PARALLEL(6) */ FROM PSX_ENPA_DATA_TEMP
        WHERE ROWID IN (
            SELECT rid
            FROM (
                SELECT /*+ PARALLEL(6) INDEX(PSX_ENPA_DATA_TEMP idx_enpa_temp_cust_unq_id_ins_ts) */
                       ROWID AS rid,
                       ROW_NUMBER() OVER (PARTITION BY CUST_UNQ_ID,SOURCE_SYSTEM_ACCOUNT_NO ORDER BY INSERT_TS DESC) AS row_num
                FROM PSX_ENPA_DATA_TEMP
            ) WHERE row_num > 1
        );
        COMMIT; -- Commit final changes

        v_status := 'C';
        v_description := 'ENPA Validation and temp data processing completed';
    ELSE
        v_status := 'C';
        v_description := 'ENPA data not found';
    END IF;

    v_error_location := 'Update counts';
    UPDATE /*+ PARALLEL(6) */ PSX_PRIME_EOD_COUNTS 
    SET YBL_ENPA_COUNT = v_enpa_count
    WHERE PSX_BATCH_ID = v_psx_batch_id;

    v_error_location := 'Insert completion status';
    INSERT /*+ PARALLEL(6) APPEND */ INTO PSX_PRIME_EOD_STATUS (
        psx_batch_id, description, STATUS, MODULE, INSERT_TIME
    ) VALUES (
        v_psx_batch_id, v_description, 'C', 'ENPA_VALIDATE_TEMP_PROC', CURRENT_TIMESTAMP
    );
    COMMIT; -- Final commit

EXCEPTION
    WHEN OTHERS THEN
        v_status := 'E';
        v_description := 'Error at ' || v_error_location || ': ' || SQLERRM;
        v_sqlerror := DBMS_UTILITY.FORMAT_ERROR_BACKTRACE;
        ROLLBACK;
END PSX_VALIDATE_ENPA_DATA_PROC;
