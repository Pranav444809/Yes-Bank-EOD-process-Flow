1. PSX_GET_PENDING_BATCH_ID

create or replace PROCEDURE PSX_GET_PENDING_BATCH_ID(
    p_batch_id OUT VARCHAR2,  -- Output parameter for batch_id
    p_status OUT VARCHAR2     -- Output parameter for status ('C' for success, 'E' for error)
)AS
        PSXBATCHID VARCHAR2(50);
        p_batch_id_Account  VARCHAR2(50);
        -- ENQUE MESSAGE VARIABLES --
    agent sys.aq$_agent := sys.aq$_agent(' ', NULL, 0);
    MESSAGE sys.aq$_jms_message;
    enqueue_options dbms_aq.enqueue_options_t;
    message_properties dbms_aq.message_properties_t;
    msgid raw(16);
    json_payload       VARCHAR2(4000); -- JSON payload

BEGIN

    BEGIN
        -- Getting the BATCH_ID
        SELECT /*+ enable_parallel_dml parallel(6) */ BATCH_ID
        INTO p_batch_id
        FROM PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T
        WHERE SL = (SELECT /*+ enable_parallel_dml parallel(6) */ MIN(sl) FROM PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T WHERE EOD_STATUS = 'P' AND DATA_CATEGORY='CUSTOMER')
        AND EOD_STATUS = 'P' AND DATA_CATEGORY='CUSTOMER';
Commit;

        -- Returning "C" in case of success
        p_status := 'C';        
    EXCEPTION
        -- Handling no data found error
         -- Handling no data found error
        WHEN NO_DATA_FOUND THEN
            p_batch_id := NULL;
            p_status := 'E';  -- Return 'E' for error

        -- Handle any other exception
        SELECT /*+ enable_parallel_dml parallel(6) */ BATCH_ID
        INTO p_batch_id_Account
        FROM PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T
        WHERE SL = (SELECT /*+ enable_parallel_dml parallel(6) */ MIN(sl) FROM PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T WHERE EOD_STATUS = 'P' AND DATA_CATEGORY='ACCOUNTS')
        AND EOD_STATUS = 'P' AND DATA_CATEGORY='ACCOUNTS';
Commit;
        -- GENERATE PSX_BATCH_ID IF CUSTOMER DATA = 0 --------
        PSXBATCHID := TO_CHAR(SYSDATE, 'YYYYMMDDHH24MISS');
        UPDATE PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T SET PSX_BATCH_ID = PSXBATCHID WHERE BATCH_ID = p_batch_id_Account AND DATA_CATEGORY='ACCOUNTS';


         -- Enqueue PSX_EOD_QUEUE
        MESSAGE := sys.aq$_jms_message.construct(dbms_aq.JMS_TEXT_MESSAGE);
        message.set_replyto(agent);
        message.set_type('tkaqpet2');
        message.set_userid('yesbnk');
        message.set_appid('plsql_enq');
        message.set_groupid('st');
        message.set_groupseq(1);
        message.set_Short_Property('systemId',1);
        message.set_Long_Property('psxIdBatchId',PSXBATCHID);  -- Use the input batch ID
        message.set_Short_Property('purposeId',99);
        message.set_string_property('PSX_BATCH_ID', PSXBATCHID); 
        message.set_text(json_payload);

    DBMS_AQ.ENQUEUE(
        queue_name => 'PROD_PRIME360DEDUPE.PSX_EOD_QUEUE',
        enqueue_options => enqueue_options,
        message_properties => message_properties,
        payload => message,
        msgid => msgid
    );
    COMMIT;

        WHEN OTHERS THEN
            p_batch_id := NULL;
            p_status := 'E';  -- Return 'E' for error
--            p_descripition := 'Error in the PSX_GET_PENDING_BATCH_ID PROCEDURE';
--            p_sqlerror := 'Error: ' || SQLERRM || ' at ' || DBMS_UTILITY.FORMAT_ERROR_BACKTRACE;
            RAISE_APPLICATION_ERROR(-20001, 'Error occurred in the PSX_GET_PENDING_BATCH_ID procedure: ' || SQLERRM);

             -- Update the EOD_STATUS in the target schema based on the provided batch IDs
    UPDATE PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T
    SET EOD_STATUS = 'E'
    WHERE BATCH_ID = p_batch_id
    AND DATA_CATEGORY='CUSTOMER';
    END;
END PSX_GET_PENDING_BATCH_ID;
