   IF sum_batch_total_rec_count = 0 THEN
   UPDATE /*+ PARALLEL(1) */ PROD_PSX_DEDUPE_STAGING.PSX_EOD_CONTROL_T
   SET EOD_STATUS = 'E',
   ERROR_DESCRIPTION = 'No Data Avaialbe for CLIP EOD Process'
   WHERE BATCH_ID = v_batch_id AND DATA_CATEGORY='CUSTOMER' AND EOD_STATUS='P';  

        COMMIT; -- Commit the changes
   RAISE total_data_count_zero_in_control_t;
   END IF;

